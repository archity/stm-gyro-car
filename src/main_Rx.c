/**
  * @brief  The application entry point.
  *
  * @retval None
  */
int main(void)
{
  /* USER CODE BEGIN 1 */

  /* USER CODE END 1 */

  /* MCU Configuration----------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_USART6_UART_Init();
  MX_TIM1_Init();
  /* USER CODE BEGIN 2 */
HAL_TIM_PWM_Start(&htim1,TIM_CHANNEL_1);
HAL_TIM_PWM_Start(&htim1,TIM_CHANNEL_2);
HAL_TIM_PWM_Start(&htim1,TIM_CHANNEL_3);

  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
		//PWM Generation
		TIM1->CCR1=duty1*period/100;
    TIM1->CCR2=duty2*period/100;
		TIM1->CCR3=duty3*period/100;
    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */
    HAL_UART_Receive_IT(&huart6,data_rx_mcu,24);    		
	
    //Receiving packets continuously from End Point XBee (EP) 
		
	  for(j=0;j<24;j++)	//24 byte frame
		{	
			if(data_rx_mcu[j]== 0x7E)			//Recognises Zigbee Header and starts processing
			{
				if(data_rx_mcu[(j+3)%24]== 0x92)   
			//Recognises x data from the wirelessly transferred frame
				{
				rx_data_msb = data_rx_mcu[(j+20)%24];
				rx_data_lsb = data_rx_mcu[(j+21)%24];
				xangle = rx_data_lsb+(rx_data_msb* 256);  
				
				//Recognises y data from the wirelessly transferred frame
				rx_data_msb_z=data_rx_mcu[(j+22)%24];
        			rx_data_lsb_z=data_rx_mcu[(j+23)%24];
				yangle = rx_data_lsb_z+ (rx_data_msb_z*256);
				

//					  LHS=	Duty Cycle to Motor
//					  RHS= I/P from xbee
//					0   ==    [xangle=7936,yangle=8937]
//					256 ==    [xangle=42496,yangle=41954]
//					512 ==    [xangle=22273,yangle=21630]
		
					
//					Duty1=Forward Right Motor
//					Duty2=Forward Left Motor
//					Duty3= Reverse Both Motors
				  
					 if((xangle>=39000 && xangle<=45000)&&(yangle>=39700 && yangle<=44000))
				{
					// Condition for staying still
					duty1=0;
					duty2=0;
					duty3=0;
					
				}
				 if((xangle>=19000 && xangle<=25500)&&( yangle>=39700 && yangle<=44000))
				{
					// Condition for Left turn
					duty1=75;      
					duty2=100;         
					duty3=0;
					
				}
				if((xangle>=5000 && xangle<=10000)&&(yangle>=39700 && yangle<=44000))
				{
					// Condition for Right turn
					duty1=100;         
					duty2=75;
					duty3=0;
					
				}
				 if((xangle>=39000 && xangle<=45000)&&( yangle>=5000 && yangle<=12000))
				{
					// Condition for going straight
					duty1=100;      
					duty2=100;         
					duty3=0;
					
				}
				if((xangle>=39000 && xangle<=45000) && (yangle>=18000&& yangle<=24000))
				{
					// Condition for going back
					duty1=0;
					duty2=0;
					duty3=100;
					
				}
				
				}

			else if(data_rx_mcu[(j+3)%24]== 0x97)
		//Recognises Acknowledgment sent by EP 
				{
				ack_count = ack_count+1;      						//Counts Acknowledgements received from EP 
				break;
				}	 
			} 
	}
}
  /* USER CODE END 3 */

}

